# 낮에 달팽이는 A 미터 올라감
# 밤에 B 미터 미끄러짐
# 나무 막대의 높이는 V 미터
# 며칠 만에 정상에 도착하는지 구하는 문제

# 하루에 실질적으로 올라가는 높이 = A - B
# 하지만 마지막 날에는 밤에 미끄러지지 않음.
# 즉, 꼭대기 도달 직전에 낮에만 A 만큼 올라가면 끝

#===

import math

# A: 낮에 올라가는 거리
# B: 밤에 미끄러지는 거리
# V: 나무 막대의 높이
A, B, V = map(int, input().split())

# 마지막 날에는 밤에 미끄러지지 않으므로, V-A까지는 '순이득'으로 계산
# 하루 순이득 = A - B
# (V - A)를 (A - B)로 나눠서 올림 처리 → 필요한 "완전한 날 수"
# 마지막 날(+1)은 낮에 A만큼 올라가서 끝
days = math.ceil((V - A) / (A - B)) + 1

print(days)

