# 숫자 1부터 시작해서 벌집처럼 육각형 모양으로
# 점점 퍼져나가는 구조에서,
# 숫자 N이 주어졌을 때
# 1번 방에서 N번 방까지 최소 몇 개의 방을 지나가야 하는지 구하는 문제

# 벌집 구조:

#    1
#   2 ~ 7
#   8 ~ 19
#  20 ~ 37
#  38 ~ 61
#  ...
# 1번 방: 무조건 시작 (1칸)
# 그 다음부터는 껍질마다 6개씩 늘어나는 구조

# 각 껍질은 이전보다 6, 12, 18, 24... 씩 커지고
# 결국 N이 몇 번째 껍질에 포함되는지를 찾는 문제!

n = int(input())  # 목표 숫자

layer = 1         # 1번 방은 무조건 첫 번째 층
max_num = 1       # 현재 층의 마지막 숫자 (시작은 1)

while n > max_num:
    max_num += 6 * layer  # 각 층마다 6×층수만큼 방 증가
    layer += 1            # 다음 층으로 이동

print(layer)


