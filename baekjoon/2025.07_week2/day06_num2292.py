n = int(input())  # 입력받은 숫자
layer = 1         # 시작 층 (1번 방)
max_num = 1       # 현재 층의 끝 방 번호

while n > max_num:        # 아직 N이 이 층에 포함 안 되면
    max_num += 6 * layer  # 다음 층의 끝 방 번호 계산
    layer += 1            # 층 수 하나 증가

print(layer)  # N이 포함된 층(=지나야 하는 방 수) 출력


