# 두 자연수 A, B를 한 줄에서 입력받기
A, B = map(int, input().split())

# ------------------------------
# 1) 최대공약수(GCD) 구하기
# ------------------------------

# 유클리드 호제법으로 GCD를 구하는 함수 정의
def gcd(a, b):
    # b가 0이 될 때까지 반복
    while b != 0:
        a, b = b, a % b   # (a, b)를 (b, a%b)로 계속 교체
    return a              # b가 0이 되면 그때의 a가 최대공약수

g = gcd(A, B)             # A와 B의 최대공약수

# ------------------------------
# 2) 최소공배수(LCM) 구하기
# ------------------------------
# 공식: LCM(A, B) = A * B // GCD(A, B)
l = A * B // g

# ------------------------------
# 3) 출력
# ------------------------------
print(g)  # 첫째 줄: 최대공약수
print(l)  # 둘째 줄: 최소공배수
